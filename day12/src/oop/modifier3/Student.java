package oop.modifier3;

public class Student {
	//필드는 Private으로 잠그기
	private String name;
	private int score;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public Student(){}
	
	
	
	
	
}
