package data;

public class Test03 {
	public static void main(String[] args) {
		
		int a = 7000*2;//7000 *2를 계산해서 a라는 이름으로 저장(대입)
		int b = 8000*3;//8000 *3을 계산해서 b라는 이름으로 저장(대입)
		
		System.out.println(a);//a에 저장된 데이터 출력
		System.out.println(b);//b에 저장된 데이터 출력
		System.out.println(a+b);//a+b를 더한 값을 출력
		
	}
}
